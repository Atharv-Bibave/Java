/*
Description:
	It returns character located at specified index within the given string.

Parameter:
	Integer(index)

Return Type:
	Character
*/

class CharAtDemo{
	public static void main(String [] args){
		String str1  = "Core2Web";

		System.out.println(str1.charAt(4));
		System.out.println(str1.charAt(0));
	
	}
}

